void ElementArray()  
{
    int index = 0, minsum = 0;  // Вводим целочисленные переменные index и minsum и загоняем в них нули
    for (int i = 0; i < array.GetLength(0); i++)  // Цикл(прохождение по строкам(0) массива) (счетчик равен 0, счетчик меньше колличества строк, шаг счетчика на 1)
    {
        int sum = 0;  // Вводим переменную sum(сумма элементов) и загоняем в нее 0(указали ее тут, чтобы после первого цикла ее значение обнулялось) 

        for (int j = 0; j < array.GetLength(1); j++)  // Цикл(прохождение по столбцам(1) массива) (счетчик равен 0, счетчик меньше колличества столбцов, шаг счетчика на 1) вводим j так как в предыдущем масиве была i
        {
            sum += array[i, j];  // В значение переменной sum загоняем ее значение плюс элементы массива(нахождение суммы элементов по столбцам)
            // sumElement= sumElement+ array[i,j];    
           
            if (i == 0)  // Если номер строки равен 0
            {
                minsum = sum;  // То значение переменной  sum загоняем в переменную minsum
            }
            else if (sum < minsum)  // Или если значение переменной sum меньше значения переменной minsum   
            {
                minsum = sum;  // То значение переменной  sum загоняем в переменную minsum
                index = i;  // Номер строки загоняем в переменную index
            }
        }
        Console.WriteLine("Сумма строк " + sum);  // Выводим в консоль надпись "Сумма строк " и значение переменной sum(сумма элементов в строках)
    }

    int line = 0;  // Вводим целочисленную переменную line и загоняем в нее 0
    for (int j = 0; j < array.GetLength(0)-1; j++)  // Цикл(прохождение по строкам(0) массива) (счетчик равен 0, счетчик меньше колличества строк, шаг счетчика на 1)
    {
        line += array[index, j];  // В значение переменной line загоняем ее значение плюс элементы массива(строки)
    }
    Console.WriteLine("Строка с минимальной суммой элементов " + index); 
}
