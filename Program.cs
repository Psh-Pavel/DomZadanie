void FlippingArray(int[,] arr) 
{
    for (int i = 0; i < arr.GetLength(0); i++)  // Цикл(прохождение по строкам(0) массива) (счетчик равен 0, счетчик меньше колличества строк, шаг счетчика на 1)
    {
        for (int j = 0; j < arr.GetLength(1); j++)  // Цикл(прохождение по столбцам(1) массива) (счетчик равен 1, счетчик меньше колличества столбцов, шаг счетчика на 1) вводим j так как в предыдущем масиве была i
        {
            if (i > 0 && i < arr.GetLength(0)-1)  // Если счетчик больше 0 и(&&) меньше колличества строк-1
            {
                int temporary = arr[i, j];  // То вводим новую целочисленнцю переменную и загоняем в нее массив 
            }
            else 
            {
                int temporary = arr[i, j]; 
                arr[i, j] = arr[(arr.GetLength(0) - 1), j]; 
                arr[(arr.GetLength(0) - 1), j] = temporary;
            }
        }
    }
}








